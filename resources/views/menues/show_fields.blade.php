    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑; color: rgb(102, 102, 102); font-size: 14px; letter-spacing: 0px;">许多雅思考生并不知道在雅思写作中怎样比较入考官的眼。在雅思写作备考过程中，考生们可以通过练习来提高熟悉度，从而更好地掌握。雅思写作真的需要大量的词汇，当遇到不会的单词就非要查字典嘛？其实不然。下面告诉小伙伴们如何去很好的表达它。</span><br>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">雅思小作文经常出现描述图标题，其分值约占写作部分的三分之一，由于其所占比例较低，经常被考生所忽略。但是事实上，这类图表描述题可控，文本使用上比较正式，语言上比较固定，因此考生们只要掌握了标准式的描写风格，以及一些固定的语言搭配，往往更加容易掌握，也是比较容易在短时间提高分数的一个题型。</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">图表描述题的难点之一是难以在短时间内找出该题需要表达的重点，其次缺少书面表达的语言和亮点。</span></span>
    </p>
    <p style=";text-indent: 36px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(255, 0, 0);letter-spacing: 0;font-size: 18px">1.寻觅小作文需要表达的重点</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">见到图形表达题，你心中就需要思考这个图形题需要表述的重点在哪里。作答时先说明再梳理数据。切不可将文章写成账本似的文字，否则，分数会很低。</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">例如：</span>The grap shows how the amount of water used worldwide changed 1900 and 2000. Throughout the century, the largest quantity of water was used for agricultural Purposes, and this increased dramatically from about 500 km to around 3,000 km in the year 2000.</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">文章的主句应该对文章要表达的趋势，量的大小作出说明，相关的数据则补充在后面。雅思小作文考虑的精髓还是查看考生的英文表达能力，精简凝练。你的描述要让阅卷者脑海中浮现出图形的样本，数量和趋势等等相关数量。如果你能达到以上这些要求，那么小作文本身也就基本上合格了。</span></span>
    </p>
    <p style=";text-indent: 36px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(255, 0, 0);letter-spacing: 0;font-size: 18px">2.了解图形的分类规律</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">图形描述题，按照标准图形来分，可以分为</span>line graph (curve), bar chart, pie chart以及table，按照表达重点来分，分为趋势类和大小类，分类依据于题中的时间列表。一般来说，出现时间短的图形题都是以描述数据变化趋势为主。</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 36px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(255, 0, 0);letter-spacing: 0;font-size: 18px">3.准备必要的表达方式</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(0, 112, 192);letter-spacing: 0;font-size: 14px">1）与趋势相关的词语：</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">上升（动词）：</span>increase, rise, clib, ascend, surge, shoot up</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">下降（动词）：</span>decrease, decline, drop, fall, descend, plunge</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">持平（动词）：</span>remain stable, keep constant, level off, flatten out</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">波动（动词）：</span>fluctuate, wave, go up and down</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">快速（形容词）：</span>considerable, significant, substantial, remarkable, dramatic</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">稳步（形容词）：</span>steady, stable, moderate, modest</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">缓慢（形容词）：</span>slight,minimal,marginal</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">顶点（名词</span>/动词）：peak</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">趋势（名词）：</span>trend, tendency</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(0, 112, 192);letter-spacing: 0;font-size: 14px">2)与大小相关的词语：</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Outnumber（动词）：在数量上超过 The demonstrators were outnumbered by the police</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Exceed（动词）：在数量上超过</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Triple（动词）：变成三倍 The fogure has tripled.</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Double（动词）：变成两倍</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Mount to（动词）：达到</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">Counterpart（名词）：对应的另一方，比较的另一方 This product is better than its counterparts.</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">此外，建议考生还应考虑一下题中数据和百分比有没有联系，如果有的话还应该添加一些数据百分比的语句。</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(0, 112, 192);letter-spacing: 0;font-size: 14px">3）相关词语</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">百分比：</span>proportion, rate, percentage, share</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">占据：</span>occupy, comprise, constitute, account, for, represent</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">数字：</span>number, amount, data,figure</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">比例：</span>rate, proportion, percentage, share</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">大约：</span>roughly, about, nearly, approximately, a little less than, slightly more than</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">各自地：</span>reapectively, for each, severally</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(0, 112, 192);letter-spacing: 0;font-size: 14px">4）注意连词的使用</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">连词在语句上起着承上启下的作用，添加连词，可以增加文章的逻辑感，使得文章更加严谨，同时，文章中连词的使用也要讲究一定的形式，一般称之为形连。</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(0, 112, 192);letter-spacing: 0;font-size: 14px">5）Integrity and Perfection</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">文章有了恰当的表达方式，还需要进行一些细节方面的修饰，添加数据时，形式要整齐划一，有不随便删除和添加数量单位，增加连词的使用，加强文章的严谨度，采用一些多样化的数据添加方式，使文章看起来有理有据，中心思想贯穿全文。</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(128, 100, 162);letter-spacing: 0;font-size: 14px"><span style="font-family:微软雅黑">主要添加方式有：</span></span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">A. 利用标点，如括号和破折号</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">B. 利用介词，如with, at, to, by等</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">C. 利用分词或从句，倒装句</span>
    </p>
    <p style=";text-indent: 28px;padding: 0;background: rgb(255, 255, 255)">
        <span style="font-family: 微软雅黑;color: rgb(102, 102, 102);letter-spacing: 0;font-size: 14px">&nbsp;</span></p>
